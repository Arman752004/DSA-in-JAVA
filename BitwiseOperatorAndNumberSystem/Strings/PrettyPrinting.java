package Strings;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 453.1234f;
        System.out.printf("formatted number is %.2f",a); //.2f will give 2 decimal value after point '.' as well as it rounds off

        System.out.printf("Hello my name is %s and I am %s","Arman","Cool");
    }
}
